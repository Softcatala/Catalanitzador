    <h2>Com configurar l'Android en català</h2>
    	<div class="download_section">

<p>
No tots els dispositius Android permeten configurar al català. Alguns fabricants suprimeixen el català del llistat de llengües disponibles, i algunes versions antigues del sistema operatiu, anterior a la 4.0, no incorporen el català. 
Per tant, depenent del model, versió de sistema operatiu, fabricant i operadora, no podreu configurar el català.</p>

Per saber si podeu configurar el dispositiu Android i, si és així, catalanitzar-lo. Seguiu les passes següents:
</p>

<ul class="general">
    <li>Pitgeu el botó per obrir el menú d'aplicacions i seleccioneu la icona Configuració (Settings si teniu l'Android configurat en anglès, Ajustes si el teniu espanyol o Paràmetres si el teniu en francès).</li>
    <li>Llisqueu avall i seleccioneu l'opció Idioma i introducció de text (Language & input en anglès, Idioma e introducción de texto en espanyol, Langue et saisie en francès).</li>
    <li>Seleccioneu la primera opció Idioma (Language en anglès, Idioma en espanyol, Langue en francès). </li>
    <li>En el llistat de llengües heu de seleccionar una de les llengües disponibles. Cerqueu el català i seleccioneu-lo. Si no hi és, significa que no es pot configurar el català en el vostre dispositiu.</li>
    <li>Pitgeu el botó Inici.</li>
</ul>

<br class="p">
<p>
Després de seleccionar el català, les aplicacions que tinguin versió en català es mostraran en català i en navegar sol·licitarem continguts en aquesta llengua.
</p>
<p>
Nota: el text exacte dels menús que cal seleccionar pot variar segons la llengua activa, de la versió del sistema operatiu o del fabricant.
</p>

<br class="p">

<p>Si teniu cap dubte o us cal ajuda per configurar el vostre mòbil o tauleta podeu adreçar-vos al <a href="http://www.softcatala.org/forum/viewforum.php?f=8&sid=04e08382c279bf4d89b5add56a481378">fòrum d'Android</a>.</p>

