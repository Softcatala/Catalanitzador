        <br/>- <a href="http://www.softcatala.org/wiki/Rebost:OpenSUSE" title="Fitxa de l'OpenSUSE al rebost de Softcatalà">Baixada de l'OpenSUSE</a>
        <br/>- <a href="http://www.softcatala.org/wiki/Rebost:Linux_Mint" title="Fitxa del Linux Mint al rebost de Softcatalà">Baixada del Linux Mint</a>
    </li>
    <li>Quan s'iniciï la instal·lació, seleccioneu el català en el segon pas</li>
    <li>A partir d'aquí tot el sistema es configurarà en català</li>
</ul>
<br class="p"/>
<h3>Selecció de llengua a l'escriptori</h3>
<ul class="general">
    <li>Segons l'entorn d'escriptori Linux que feu servir, el procediment de selecció de llengua és diferent</li>
    <li>Heu d'accedir a les preferències generals del sistema i cercar les seccions de «Locale», «Idioma» o «Llengua» i 
    des d'allà seleccionar el català</li>
</ul>

<br class="p"/>
