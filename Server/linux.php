</p>

<h3>Selecció de llengua durant la instal·lació</h3>
<ul class="general">
    <li>Podeu baixar distribucions des del rebost de Softcatalà:<br/>
        - <a href="http://www.softcatala.org/wiki/Rebost:Ubuntu" title="Fitxa de l'Ubuntu al rebost de Softcatalà">Baixada de l'Ubuntu</a>
